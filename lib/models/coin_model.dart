class Coin {
  final String id;
  final String symbol;
  final String name;
  final String imageUrl;
  final int currentPrice;
  final int marketCap;
  final int marketCapRank;
  final int highPrice;
  final int lowPrice;

  Coin({
    this.id,
    this.symbol,
    this.name,
    this.imageUrl,
    this.currentPrice,
    this.marketCap,
    this.marketCapRank,
    this.highPrice,
    this.lowPrice,
  });
}
